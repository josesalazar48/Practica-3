    private void buttonregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonregistrarActionPerformed
        // TODO add your handling code here:
    try {
        String barrio = txtbarrio.getText();
        int pisos = Integer.parseInt(txtpisos.getText());
        float largo = Float.parseFloat(txtlargo.getText());
        float ancho = Float.parseFloat(txtancho.getText());

        // Generar número automático
        int nrocasa = Integer.parseInt(cs.numerarCasas());

        boolean registrado = cs.registroCasa(nrocasa, barrio, largo, ancho, pisos);

        if (registrado) {
            cargarTabla(); // Actualizar tabla
        } else {
            JOptionPane.showMessageDialog(this, "Error al registrar");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Datos inválidos: " + e.getMessage());
    }
    }//GEN-LAST:event_buttonregistrarActionPerformed

