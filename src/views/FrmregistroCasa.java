    private void cargarTabla() {
    String[][] datos = cs.listar();

    if (datos != null) {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0); // limpiar tabla

        for (int i = 0; i < datos.length; i++) {
            String barrio = datos[i][1];
            int pisos = Integer.parseInt(datos[i][4]);
            float largo = Float.parseFloat(datos[i][2]);
            float ancho = Float.parseFloat(datos[i][3]);

            String homonima = cs.esHomonima(i,pisos, largo, ancho) ? "Homonima" : "";

            Object[] fila = { barrio, pisos, largo, ancho, homonima };
            modelo.addRow(fila);
        }
    }
}

